    private void button7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button7ActionPerformed
        int rowcount = tbsachmuon.getRowCount();
        if (rowcount == 0) {
            Notification panel = new Notification((JFrame) SwingUtilities.getWindowAncestor(this), Notification.Type.WARNING, Notification.Location.TOP_CENTER, "Chưa chọn sách!!");
            panel.showNotification();
            return;
        }
        for (int row = 0; row < rowcount; row++) {
            if (tbsachmuon.getValueAt(row, 2).toString().equals("0")) {
                Notification panel = new Notification((JFrame) SwingUtilities.getWindowAncestor(this), Notification.Type.WARNING, Notification.Location.TOP_CENTER, "Nhập thiếu số lượng sách " + tbsachmuon.getValueAt(row, 1).toString());
                panel.showNotification();
                return;
            }
//            int slt = Integer.valueOf(txtsoluongtrats.getText());
//            int slm = Integer.valueOf(txtsoluongmts.getText());
            if (Integer.valueOf(tbsachmuon.getValueAt(row, 2).toString()) < Integer.valueOf(tbsachmuon.getValueAt(row, 3).toString())) {
                Notification panel = new Notification((JFrame) SwingUtilities.getWindowAncestor(this), Notification.Type.WARNING, Notification.Location.TOP_CENTER, "Số lượng sách không đủ: mã sách " + tbsachmuon.getValueAt(row, 1).toString());
                panel.showNotification();
                return;
            }
        }
        if (txttendgms.getText().equals("")) {
            Notification panel = new Notification((JFrame) SwingUtilities.getWindowAncestor(this), Notification.Type.WARNING, Notification.Location.TOP_CENTER, "Chưa chọn độc giả!!");
            panel.showNotification();
            return;
        }
        String sopm = "";
        try {
            sopm += MuonTra_Controller.autosophieumuon();
            String sql = "insert into chitietmuontra values";
            for (int row = 0; row < rowcount; row++) {
                if (row < rowcount - 1) {
                    sql += "('" + sopm + "', '" + tbsachmuon.getValueAt(row, 0).toString() + "', '" + tbsachmuon.getValueAt(row, 3).toString() + "', 0, null),";
                } else {
                    sql += "('" + sopm + "', '" + tbsachmuon.getValueAt(row, 0).toString() + "', '" + tbsachmuon.getValueAt(row, 3).toString() + "', 0, null)";
                }
            }
            Date currentDate = new Date();
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String ngay = dateFormat.format(currentDate);
            Calendar calendar = Calendar.getInstance();
            calendar.setTime(currentDate);
            calendar.add(Calendar.DAY_OF_MONTH, 15);
            Date newDate = calendar.getTime();
            String ngay15 = dateFormat.format(newDate);
            MuonTra_Controller.ThemMoiPhieuMuon(sopm, txtmadgms.getText(), login.manv, ngay, ngay15);
            MuonTra_Controller.ThemMoiChiTietNhapSach(sql);
            Notification panel = new Notification((JFrame) SwingUtilities.getWindowAncestor(this), Notification.Type.WARNING, Notification.Location.TOP_CENTER, txttendgms.getText() + " đã mượn sách thành công");
            panel.showNotification();
            try {
                arrDocGia = DocGia_Controller.LayNguonDG(txtmadgms.getText());
                if (arrDocGia.size() == 0) {
                    panel = new Notification((JFrame) SwingUtilities.getWindowAncestor(this), Notification.Type.WARNING, Notification.Location.BOTTOM_RIGHT, "Không tìm thấy độc giả");
                    panel.showNotification();
                    txtmadgms.requestFocus();
                } else {
                    for (Model_DocGia dg : arrDocGia) {
                        madg = dg.getMaDG();
                        tendg = dg.getTenDG();
                        txttendgms.setText(tendg);
                    }
                    txtsosachmuonms.setText("" + MuonTra_Controller.getslSachMuon(madg));
                    txtsoquahanms.setText("" + MuonTra_Controller.getslSachMuonQuaHan(madg));
                }
            } catch (SQLException ex) {
                Logger.getLogger(Form_MuonTra.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Form_Sach.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_button7ActionPerformed

