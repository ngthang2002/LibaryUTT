    public void LayNguonMaSach() {

        try {
            arrSach = MuonTra_Controller.LayNguonSach("Sách");
        } catch (SQLException ex) {
            Logger.getLogger(Form_Sach.class.getName()).log(Level.SEVERE, null, ex);
        }
        cbbmasach.removeAllItems();
        cbbmasach.addItem("Sách");
        for (int i = 0; i < arrSach.size(); i++) {

            cbbmasach.addItem(arrSach.get(i).getMaSach());
        }
    }

    public void LayNguonSach(String masach) {
        try {
            arrSach = MuonTra_Controller.LayNguonSach(masach);
        } catch (SQLException ex) {
            Logger.getLogger(Form_Sach.class.getName()).log(Level.SEVERE, null, ex);
        }
        tblDanhSachms.setRowCount(0);
        tbSach.setModel(tblDanhSachms);
        arrSach.forEach(p
                -> {
            tblDanhSachms.addRow(new Object[]{
                p.getMaSach(), p.getTenSach(), p.getTenTL(), p.getTacGia(), p.getSoLuong()
            });
        }
        );
    }

    public void LayNguonTinhTrangSach(String madg) {
        try {
            arrTinhTrangSach = MuonTra_Controller.LayNguonTinhTrangSach(madg);
        } catch (SQLException ex) {
            Logger.getLogger(Form_Sach.class.getName()).log(Level.SEVERE, null, ex);
        }
        tblTinhTrangSachh.setRowCount(0);
        tbtinhtrangsach.setModel(tblTinhTrangSachh);
        arrTinhTrangSach.forEach(p
                -> {
            tblTinhTrangSachh.addRow(new Object[]{
                p.getSoPM(), p.getMaSach(), p.getTenSach(), p.getSoLuong(), p.getTinhTrang(), p.getHanTra()
            });
        }
        );
    }

