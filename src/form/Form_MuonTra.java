    private void bttrasachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttrasachActionPerformed
        if (txtsoluongtrats.getText().equals("")) {
            Notification panel = new Notification((JFrame) SwingUtilities.getWindowAncestor(this), Notification.Type.WARNING, Notification.Location.TOP_CENTER, "Chưa nhập số lượng sách trả!");
            panel.showNotification();
            return;
        }

        try {
            int slt = Integer.valueOf(txtsoluongtrats.getText());
            int slm = Integer.valueOf(txtsoluongmts.getText());
            System.out.println(slt);
            if (slt > slm || slt < 0) {
                Notification panel = new Notification((JFrame) SwingUtilities.getWindowAncestor(this), Notification.Type.WARNING, Notification.Location.TOP_CENTER, "Yêu cầu sô lượng trả phải nhỏ hơn hoặc bằng số lượng sách mượn > 0!");
                panel.showNotification();
                return;
            } else {
                Date currentDate = new Date();
                SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                String ngay = dateFormat.format(currentDate);
                String tinhtrang = "";
                if (slt == slm) {
                    tinhtrang += "1";
                } else {
                    tinhtrang += "1" + slt;
                }
                int index = tbtinhtrangsach.getSelectedRow();
                MuonTra_Controller.UpdateChiTietMuonTra(tbtinhtrangsach.getValueAt(index, 0).toString(), txtmasachts.getText(), tinhtrang, slt, ngay);
                LayNguonTinhTrangSach(madg);
                txtmasachts.setText("");
                txttensachts.setText("");
                txtsoluongmts.setText("");
                txtsoluongtrats.setText("");
            }
        } catch (NumberFormatException ex) {
            Notification panel = new Notification((JFrame) SwingUtilities.getWindowAncestor(this), Notification.Type.WARNING, Notification.Location.TOP_CENTER, "Yêu cầu sô lượng phải là kiểu số nguyên!");
            panel.showNotification();
            return;
        }
    }//GEN-LAST:event_bttrasachActionPerformed

