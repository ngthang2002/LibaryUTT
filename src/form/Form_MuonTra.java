    private void btktmadgmsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btktmadgmsActionPerformed
        if (txtmadgms.getText().length() <= 0) {
            Notification panel = new Notification((JFrame) SwingUtilities.getWindowAncestor(this), Notification.Type.WARNING, Notification.Location.BOTTOM_RIGHT, "Bạn chưa nhập mã độc giả");
            panel.showNotification();
            txtmadgms.requestFocus();
            return;
        }
        try {
            arrDocGia = DocGia_Controller.LayNguonDG(txtmadgms.getText());
            if (arrDocGia.size() == 0) {
                Notification panel = new Notification((JFrame) SwingUtilities.getWindowAncestor(this), Notification.Type.WARNING, Notification.Location.BOTTOM_RIGHT, "Không tìm thấy độc giả");
                panel.showNotification();
                txtmadgms.requestFocus();
            } else {
                for (Model_DocGia dg : arrDocGia) {
                    madg = dg.getMaDG();
                    tendg = dg.getTenDG();
                    txttendgms.setText(tendg);
                }
                txtsosachmuonms.setText("" + MuonTra_Controller.getslSachMuon(madg));
                txtsoquahanms.setText("" + MuonTra_Controller.getslSachMuonQuaHan(madg));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Form_MuonTra.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btktmadgmsActionPerformed

